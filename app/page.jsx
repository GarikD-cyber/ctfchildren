function recordMistake(profile, updateProfile, missionId) {
  const count = (profile.attempts[missionId] || 0) + 1;
  updateProfile((p) => ({
    ...p,
    attempts: { ...p.attempts, [missionId]: count },
    hints: count >= 2 ? { ...p.hints, [missionId]: true } : p.hints,
  }));
  return count;
}

